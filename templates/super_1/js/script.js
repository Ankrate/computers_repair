$(function () {
  // Мессенджеры
  // $(".messenger.puls").css("display","none");

  setTimeout(function () {
    $(".messenger.puls").removeClass("fade");
  }, 5000);
  var menuBtn = $(".messenger-btn"),
    menu = $(".messenger-links");
  menuBtn.on("click", function () {
    if (menuBtn.hasClass("active")) {
      $(".messenger").addClass("puls");
      menuBtn.removeClass("active");
      menuBtn.addClass("on-active");
      setTimeout(function () {
        $(".messenger .messenger-title").removeClass("uk-hidden");
      }, 5000);
    } else {
      menuBtn.removeClass("on-active");
      $(".messenger").removeClass("puls");
      $(".messenger .messenger-title").addClass("uk-hidden");
      menuBtn.addClass("active");
    }
    menuBtn.find("span").each(function () {
      if ($(this).hasClass("uk-hidden")) {
        // $(".messenger-btn").toggleClass("active");
        $(this).removeClass("uk-hidden");
      } else {
        $(this).addClass("uk-hidden");
      }
    });
    if (menu.hasClass("show")) {
      menu.removeClass("show");
    } else {
      menu.addClass("show");
    }
  });
});
